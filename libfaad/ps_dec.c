  /*{{{*/
  static void hybrid_free(hyb_info *hyb)
  {
      uint8_t i;

    if (!hyb) return;

      if (hyb->work)
          faad_free(hyb->work);

      for (i = 0; i < 5; i++)
      {
          if (hyb->buffer[i])
              faad_free(hyb->buffer[i]);
      }
      if (hyb->buffer)
          faad_free(hyb->buffer);

      for (i = 0; i < hyb->frame_len; i++)
      {
          if (hyb->temp[i])
              faad_free(hyb->temp[i]);
      }
      if (hyb->temp)
          faad_free(hyb->temp);

    faad_free(hyb);
  }
